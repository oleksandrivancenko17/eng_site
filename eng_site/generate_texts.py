def generate_json_for_batch(batch_data):
    prompt = f"""
            Ти професійний лінгвіст, методист та викладач англійської мови. 
        Твоє завдання — створити унікальні навчальні тексти для читання різного рівня складності для додатку з вивчення англійської.
        
        Створи по 1 тексту для кожної теми, oсь список тем та бажаних рівнів для генерації: 
        {batch_data}
        
        Твої тексти повинні суворо відповідати стандартам CEFR для вказаного рівня (використовуй відповідну граматику, часи та лексику: просту для A1-A2, складні конструкції та ідіоми для C1-C2).
        
        Поверни суворий масив JSON об'єктів. Кожен об'єкт повинен мати строго таку структуру:
        [
          {{
            "title": "Назва тексту англійською мовою",
            "description": "Короткий опис того, про що текст, українською мовою (1-2 речення)",
            "content": "Сам текст англійською мовою. Довжина: від 50 слів для A1 до 150-200 слів для C2.",
            "level": "Один із рівнів складності: A1, A2, B1, B2, C1, C2",
            "category": "Назва категорії українською (наприклад: Повсякденне життя, Подорожі, Технології, Наука, Історія тощо)"
          }}
        ]
        
        Важливі правила:
        1. Ніколи не використовуй Markdown форматування для тексту всередині JSON (ніяких зірочок ** чи переносів рядків \n, текст має бути суцільним).
        2. Поверни ТІЛЬКИ валідний JSON масив без жодних додаткових коментарів до чи після нього.
    """

    for model_name in MODELS_TO_TRY:
        try:
            response = client.models.generate_content(
                model=model_name,
                contents=prompt,
                config=types.GenerateContentConfig(
                    response_mime_type="application/json",
                )
            )

            if not response.text:
                continue

            return json.loads(response.text)


        except Exception as e:

            error_msg = str(e)

            if '429' in error_msg or 'RESOURCE_EXHAUSTED' in error_msg:
                print(f"⚠️ Ліміт для {model_name} вичерпано. Перемикаємось...")
                continue

            elif '503' in error_msg or 'UNAVAILABLE' in error_msg:
                print(f"⚠️ Модель {model_name} перевантажена (503). Перемикаємось...")
                time.sleep(5)
                continue

            else:
                print(f"❌ Невідома помилка для {model_name}: {e}")
                continue

    print("🛑 ВСІ МОДЕЛІ ВИЧЕРПАНО! Сесію доведеться зупинити.")
    return None


